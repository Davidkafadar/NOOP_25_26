package pckg_simple_remote;

public class GarageDoor {


    public void open() {
        System.out.println("Opening Garage Door");
    }

    public void lightOn() {
        System.out.println("Turning Garage light on");
    }

    public void close() {
        System.out.println("Closing Garage Door");
    }

    public void lightOff() {
        System.out.println("Turning Garage light off");
    }

}
